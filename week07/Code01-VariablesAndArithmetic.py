#!/usr/bin/env python3

# Jargon used in Python for variable is identifier
x = 3
print(type(x))
x = 3.14
print(type(x))
x = "Hello World!"
print(type(x))

# Arithmetic operators
x = 7
y = 4

print('Sum:', x + y)
print('Difference:', x - y)
print('Product:', x * y)
print("x^y:", x ** y)
print("True Division:", x / y)
print('Floor Division:', x // y)
print("True Division (negative):", -13 / 4)
print("Floor Division (negative):", -13 // 4)
print("Modulo:", x % y)
print('sqrt(9):', 9 ** (1/2))

